/**
  * @brief This function handles CAN1 RX0 interrupts.
  */
void CAN1_RX0_IRQHandler(void)
{
  /* USER CODE BEGIN CAN1_RX0_IRQn 0 */

  /* USER CODE END CAN1_RX0_IRQn 0 */
  HAL_CAN_IRQHandler(&hcan1);
  /* USER CODE BEGIN CAN1_RX0_IRQn 1 */
	uint8_t id, cmd;
	HAL_CAN_GetRxMessage(&hcan1, CAN_RX_FIFO0, &RxHeader, RxData);
	
	if(RxHeader.IDE == CAN_ID_EXT)
	{
		id = RxHeader.ExtId & 0xFF;
		cmd = RxHeader.ExtId >> 8;
		if(id == IVC_ID)
		{
			switch (cmd)
			{
				default:
					break;
			}
		}
		else if (id == CONTROLLER_ID)
		{
			switch(cmd)
			{
				#ifndef USE_PULSER
				case 0xAA:
					rpm = (RxData[0] << 8) | RxData[1];
					motorCurrent = (RxData[2] << 8) | RxData[3];
					controllerTemperature = (RxData[4] << 8) | RxData[5];
					totalVoltController = (RxData[6] << 8) | RxData[7];
					break;
				case 0xAB:
					d_battCurrentController = (RxData[0] << 8) | RxData[1];
					motorTemperature = (RxData[2] << 8) | RxData[3];
					errorCode = RxData[4];
					break;
				default:
					break;
				#endif
			}
		}
		else if (id == BMS_ID)
		{
			switch (cmd)
			{
				case 0x1C:
					packSOC = RxData[0] << 8 | RxData[1];
					packSOC = packSOC / 10;
					break;
				case 0x18:
					lowestVolt = (RxData[0] << 8) | (RxData[1]);
					totalVolt = (RxData[4] << 24) | (RxData[5] << 16) | (RxData[6] << 8) | (RxData[7]);
					break;
				case 0x19:
					current = (RxData[0] << 8) | RxData[1];
					temperature[0] = (RxData[4] << 8) | RxData[5];
					temperature[1] = (RxData[6] << 8) | RxData[7];
					break;
				case 0x1B:
					temperature[2] = (RxData[0] << 8) | RxData[1];
					current |= ((RxData[7] & 0x01) << 15);
					break;
				default:
					break;
			}
		}
		else if (id == BMS_ID_2)
		{
			switch (cmd)
			{
				case 0x29:
					temperature[2] = RxData[0] << 8 | RxData[1];
					temperature[3] = RxData[2] << 8 | RxData[3];
					break;
				default:
					break;
			}
		}
	}

  /* USER CODE END CAN1_RX0_IRQn 1 */
}

